export function getPreferredLanguage({ storedLanguage, browserLanguages = [] } = {}) {
  if (supportedLanguages.includes(storedLanguage)) {
    return storedLanguage;
  }

  for (const browserLanguage of browserLanguages) {
    const normalizedLanguage = String(browserLanguage).toLowerCase();

    if (normalizedLanguage.startsWith('fr')) {
      return 'fr';
    }

    if (normalizedLanguage.startsWith('en')) {
      return 'en';
    }
  }

  return defaultLanguage;
}

export function t(key, language = defaultLanguage) {
  return translations[language]?.[key] ?? translations[defaultLanguage][key] ?? '';
}
